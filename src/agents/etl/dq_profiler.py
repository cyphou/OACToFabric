"""Data quality profiling notebook generator.

Generates a PySpark notebook template that runs data quality checks
on migrated Delta tables:  null %, distinct count, outlier detection,
type validation, and summary statistics.
"""

from __future__ import annotations

import json
import logging
from dataclasses import dataclass, field

logger = logging.getLogger(__name__)


@dataclass
class DQCheck:
    """Single data quality check definition."""

    table_name: str
    column_name: str
    check_type: str  # null_pct, distinct, min_max, outlier, type_mismatch
    expected: str = ""
    threshold: float = 0.0


@dataclass
class DQProfile:
    """Data quality profile for a table."""

    table_name: str
    checks: list[DQCheck] = field(default_factory=list)
    warnings: list[str] = field(default_factory=list)


@dataclass
class DQNotebook:
    """Generated DQ profiling notebook."""

    cells: list[str] = field(default_factory=list)
    warnings: list[str] = field(default_factory=list)

    def to_notebook_json(self) -> str:
        """Generate Jupyter notebook JSON (.ipynb)."""
        nb_cells = []
        for cell_code in self.cells:
            nb_cells.append({
                "cell_type": "code",
                "source": cell_code.split("\n"),
                "metadata": {},
                "outputs": [],
                "execution_count": None,
            })
        notebook = {
            "nbformat": 4,
            "nbformat_minor": 5,
            "metadata": {
                "kernelspec": {
                    "display_name": "Python 3",
                    "language": "python",
                    "name": "python3",
                },
                "language_info": {"name": "python", "version": "3.11"},
            },
            "cells": nb_cells,
        }
        return json.dumps(notebook, indent=2)

    @property
    def cell_count(self) -> int:
        return len(self.cells)


