_NUMERIC_TYPES = frozenset({
    "int", "integer", "bigint", "smallint", "tinyint",
    "float", "double", "decimal", "numeric", "number",
    "real", "money", "int64", "float64",
})


def generate_dq_notebook(
    table_inventory: list[dict],
    include_outliers: bool = True,
) -> DQNotebook:
    """Generate a complete DQ profiling notebook.

    Args:
        table_inventory: List of table dicts, each with:
            - name (str): Table name
            - columns (list[dict]): Each with name, data_type
        include_outliers: Whether to add outlier detection cells

    Returns:
        DQNotebook with generated cells
    """
    notebook = DQNotebook()
    notebook.cells.append(_HEADER_CELL)

    for table in table_inventory:
        table_name = table.get("name", "")
        if not table_name:
            continue

        columns = table.get("columns", [])
        col_names = [c.get("name", "") for c in columns if c.get("name")]
        numeric_cols = [
            c.get("name", "")
            for c in columns
            if c.get("data_type", "").lower() in _NUMERIC_TYPES and c.get("name")
        ]

        if not col_names:
            notebook.warnings.append(f"Table {table_name}: no columns found")
            continue

        # Row count
        notebook.cells.append(_row_count_cell(table_name))

        # Null checks
        notebook.cells.append(_null_check_cell(table_name, col_names))

        # Distinct counts
        notebook.cells.append(_distinct_check_cell(table_name, col_names))

        # Outlier detection for numeric columns
        if include_outliers and numeric_cols:
            notebook.cells.append(_outlier_check_cell(table_name, numeric_cols))

    # Summary cell
    notebook.cells.append(_SUMMARY_CELL)

    logger.info(
        "Generated DQ notebook: %d cells for %d tables",
        notebook.cell_count, len(table_inventory),
    )
    return notebook
